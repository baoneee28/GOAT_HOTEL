CREATE TABLE dbo.users (id INT IDENTITY(1,1) PRIMARY KEY, full_name NVARCHAR(100) NOT NULL, email NVARCHAR(100) NOT NULL UNIQUE, password NVARCHAR(255) NOT NULL, phone NVARCHAR(15) NULL, image NVARCHAR(255) NULL, role NVARCHAR(20) NOT NULL DEFAULT 'customer', created_at DATETIME2 NOT NULL DEFAULT GETDATE());
CREATE TABLE dbo.room_types (id INT IDENTITY(1,1) PRIMARY KEY, type_name NVARCHAR(100) NOT NULL, price_per_night FLOAT NOT NULL, capacity INT NOT NULL, description NVARCHAR(MAX) NULL, image NVARCHAR(MAX) NULL);
CREATE TABLE dbo.rooms (id INT IDENTITY(1,1) PRIMARY KEY, room_number NVARCHAR(10) NOT NULL UNIQUE, type_id INT NULL FOREIGN KEY REFERENCES dbo.room_types(id) ON DELETE SET NULL, status NVARCHAR(20) NOT NULL DEFAULT 'available');
CREATE TABLE dbo.items (id INT IDENTITY(1,1) PRIMARY KEY, name NVARCHAR(100) NOT NULL, image NVARCHAR(255) NULL);
CREATE TABLE dbo.room_type_items (type_id INT NOT NULL FOREIGN KEY REFERENCES dbo.room_types(id) ON DELETE CASCADE, item_id INT NOT NULL FOREIGN KEY REFERENCES dbo.items(id) ON DELETE CASCADE, PRIMARY KEY (type_id, item_id));
CREATE TABLE dbo.bookings (id INT IDENTITY(1,1) PRIMARY KEY, user_id INT NULL FOREIGN KEY REFERENCES dbo.users(id) ON DELETE SET NULL, total_price FLOAT NULL, status NVARCHAR(20) NOT NULL DEFAULT 'pending', created_at DATETIME2 NOT NULL DEFAULT GETDATE());
CREATE TABLE dbo.booking_details (id INT IDENTITY(1,1) PRIMARY KEY, booking_id INT NOT NULL FOREIGN KEY REFERENCES dbo.bookings(id) ON DELETE CASCADE, room_id INT NULL FOREIGN KEY REFERENCES dbo.rooms(id) ON DELETE SET NULL, price_at_booking FLOAT NOT NULL, check_in DATETIME2 NULL, check_out DATETIME2 NULL, check_in_actual DATETIME2 NULL, check_out_actual DATETIME2 NULL, total_hours FLOAT NOT NULL DEFAULT 0.0);
CREATE TABLE dbo.payments (id INT IDENTITY(1,1) PRIMARY KEY, booking_id INT NOT NULL FOREIGN KEY REFERENCES dbo.bookings(id) ON DELETE CASCADE, amount FLOAT NOT NULL, payment_method NVARCHAR(50) NOT NULL, payment_date DATETIME2 NOT NULL DEFAULT GETDATE(), status NVARCHAR(20) NOT NULL DEFAULT 'completed');
CREATE TABLE dbo.services (id INT IDENTITY(1,1) PRIMARY KEY, name NVARCHAR(100) NOT NULL, price FLOAT NOT NULL, description NVARCHAR(MAX) NULL, image NVARCHAR(255) NULL);
CREATE TABLE dbo.booking_services (id INT IDENTITY(1,1) PRIMARY KEY, booking_id INT NOT NULL FOREIGN KEY REFERENCES dbo.bookings(id) ON DELETE CASCADE, service_id INT NULL FOREIGN KEY REFERENCES dbo.services(id) ON DELETE SET NULL, quantity INT NOT NULL DEFAULT 1, price_at_booking FLOAT NOT NULL, created_at DATETIME2 NOT NULL DEFAULT GETDATE());
CREATE TABLE dbo.reviews (id INT IDENTITY(1,1) PRIMARY KEY, user_id INT NULL FOREIGN KEY REFERENCES dbo.users(id) ON DELETE SET NULL, room_type_id INT NULL FOREIGN KEY REFERENCES dbo.room_types(id) ON DELETE SET NULL, booking_id INT NULL FOREIGN KEY REFERENCES dbo.bookings(id) ON DELETE SET NULL, rating INT NOT NULL CHECK (rating >= 1 AND rating <= 5), comment NVARCHAR(MAX) NULL, created_at DATETIME2 NOT NULL DEFAULT GETDATE());
CREATE TABLE dbo.news (id INT IDENTITY(1,1) PRIMARY KEY, title NVARCHAR(255) NOT NULL, slug NVARCHAR(255) NOT NULL UNIQUE, summary NVARCHAR(MAX) NULL, content NVARCHAR(MAX) NULL, image NVARCHAR(MAX) NULL, created_at DATETIME2 NOT NULL DEFAULT GETDATE());
CREATE TABLE dbo.featured_room_types (id INT IDENTITY(1,1) PRIMARY KEY, room_type_id INT NOT NULL FOREIGN KEY REFERENCES dbo.room_types(id) ON DELETE CASCADE, display_order INT NOT NULL DEFAULT 0, created_at DATETIME2 NOT NULL DEFAULT GETDATE());
CREATE TABLE dbo.featured_news (id INT IDENTITY(1,1) PRIMARY KEY, news_id INT NOT NULL FOREIGN KEY REFERENCES dbo.news(id) ON DELETE CASCADE, display_order INT NOT NULL DEFAULT 0, created_at DATETIME2 NOT NULL DEFAULT GETDATE());
GO
